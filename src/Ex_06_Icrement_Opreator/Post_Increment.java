package Ex_06_Icrement_Opreator;

public class Post_Increment {
    public static void main(String[] args) {
        int a = 10;
        System.out.println(++a);
        System.out.println(a);

        // post increement

        int b= 10;
        System.out.println(b++);
        System.out.println(b);

    }
}
